// ─── Constants ────────────────────────────────────────────────────────────────

const BASE_WIDTH = 483;
const BASE_HEIGHT = 322;

// ─── Main Component ───────────────────────────────────────────────────────────

export default function WalletAnimation() {
  const outerRef = useRef<HTMLDivElement>(null);
  const [scale, setScale] = useState(1);
  const [hovered, setHovered] = useState(false);

  const recalcScale = useCallback(() => {
    if (!outerRef.current) return;
    setScale(Math.min(1, outerRef.current.offsetWidth / BASE_WIDTH));
  }, []);

  useEffect(() => {
    const el = outerRef.current;
    if (!el) return;
    const ro = new ResizeObserver(recalcScale);
    ro.observe(el);
    recalcScale();
    return () => ro.disconnect();
  }, [recalcScale]);

  return (
    <div ref={outerRef} style={{ width: "100%" }}>
      {/* scaleWrapper: collapses layout height to match the scaled output */}
      <div
        style={{
          width: BASE_WIDTH,
          height: Math.round((BASE_HEIGHT + 120) * scale),
          transform: `scale(${scale})`,
          transformOrigin: "top left",
          overflow: "visible",
        }}
      >
        {/* scene: original dimensions — only hover/fan animations live here */}
        <div
          style={{
            position: "relative",
            width: BASE_WIDTH,
            height: BASE_HEIGHT,
            cursor: "pointer",
            marginTop: 80,
            marginBottom: 40,
            transition: "transform 0.4s cubic-bezier(0.34, 1.56, 0.64, 1)",
            transform: hovered ? "scale(1.05)" : "scale(1)",
          }}
          onMouseEnter={() => setHovered(true)}
          onMouseLeave={() => setHovered(false)}
          onTouchStart={(e) => {
            e.preventDefault();
            setHovered((v) => !v);
          }}
        >
          {/* Card 1 — fans furthest left */}
          <div
            style={{
              position: "absolute",
              inset: 0,
              display: "flex",
              alignItems: "center",
              justifyContent: "center",
              zIndex: 2,
              transformOrigin: "30% 90%",
              transition: "transform 0.55s cubic-bezier(0.34, 1.56, 0.64, 1)",
              transform: hovered
                ? "translateY(-72px) rotate(-20deg)"
                : "translateY(0px) rotate(0deg)",
              willChange: "transform",
            }}
          >
            <Card1SVG />
          </div>

          {/* Card 2 — fans slightly left */}
          <div
            style={{
              position: "absolute",
              inset: 0,
              display: "flex",
              alignItems: "center",
              justifyContent: "center",
              zIndex: 1,
              transformOrigin: "30% 90%",
              transition: "transform 0.45s cubic-bezier(0.34, 1.56, 0.64, 1) 0.05s",
              transform: hovered
                ? "translateY(-44px) rotate(-9deg)"
                : "translateY(0px) rotate(0deg)",
              willChange: "transform",
            }}
          >
            <Card2SVG />
          </div>

          {/* Wallet — always on top */}
          <div style={{ position: "absolute", inset: 0, zIndex: 10 }}>
            <WalletSVG />
          </div>
        </div>
      </div>
    </div>
  );
}